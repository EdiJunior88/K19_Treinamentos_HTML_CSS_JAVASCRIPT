//Exemplo de uso dos operadores aritméticos

var umMaisUm = 1 + 1;
//umMaisUm = 2

var tresVezesDois = 3 * 2;
//tresVezesDois = 6

var quatroDivididoPorDois = 4 / 2;
//quatroDivididoPorDois = 2

var seisModuloCindo = 6 % 5;
//seisModuloCinco = 1

var x = 7;

x = x + 1 * 2;
//x = 9

x = x - 3;
//x = 6

x = x / (6 - 2 - (3 * 5) / (16 - 1));
//x = 2

/* O módulo de um número x, na matemática, é o valor numérico de x
desconsiderando o seu sinal (valor absoluto). Na matemática expressamos
o módulo da seguinte forma: |-2| = 2.
Em linguagens de programação, o módulo de um número é o resto da divisão
desse número por outro. No exemplo acima, o resto da divisão de 6 por 5
é igual a 1. Além disso, lemos a expressão 6 % 5 da seguinte forma: seis
módulo cinco. */

/* As operações aritméticas em JavaScript obedecem as mesmas regras da
matemática com relação à precedência dos operadores e parênteses. Portanto,
as operações são resolvidas a partir dos parênteses mais internos até os
mais externos, primeiro resolvemos as multiplicações, divisões e os módulos.
Em seguida, resolvemos as adições e subtrações. */